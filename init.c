// init: The initial user-level program

#include "types.h"
#include "stat.h"
#include "user.h"
#include "fcntl.h"

char *argv[] = { "sh", 0 };

int
main(void)
{
  int pid, wpid;

  if(open("console", O_RDWR) < 0){
    mknod("console", 1, 1);
    open("console", O_RDWR);
  }
  dup(0);  // stdout
  dup(0);  // stderr

  #ifdef RR
    printf(1,"Round Robin Scheduling\n");
  #else
  #ifdef FCFS
    printf(1,"FCFS Scheduling\n");
  #else
  #ifdef PBS
    printf(1,"PBS Scheduling\n");
  #else
  #ifdef MLFQ
    printf(1,"MLFQ Scheduling\n");
  #endif
  #endif
  #endif
  #endif

  for(;;){
    printf(1, "init: starting sh\n");
    pid = fork();
    if(pid < 0){
      printf(1, "init: fork failed\n");
      exit();
    }
    if(pid == 0){
      exec("sh", argv);
      printf(1, "init: exec sh failed\n");
      exit();
    }
    while((wpid=wait()) >= 0 && wpid != pid)
      printf(1, "zombie!\n");
  }
}
